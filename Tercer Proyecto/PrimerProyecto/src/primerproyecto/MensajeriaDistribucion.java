    private void EnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarActionPerformed
        
        String asunto = Asunto.getText().toString();
        String nombreLista = NombreLista.getText().toString();
        String mensaje =  Mensaje.getText().toString();
        if(asunto.length()>0 &&nombreLista.length()>0&&mensaje.length()>0)
        {
            TercerProyecto t = new TercerProyecto();
            boolean condicion = t.ValidarLista(nombreLista);
            if(condicion == true)
            {
                DateFormat sdf = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
                DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
                LocalDateTime now = LocalDateTime.now();
                String fecha = dtf.format(now)+"";
                //(String emisor,String receptor,String mensaje,String asunto,String adjunto,String fecha)
                String emisor = "";
                ArrayList<String> m = t.MostrarAmigos(nombreLista);                
                for (int i = 0; i < m.size(); i++) 
                {                  
                    String receptor ="";
                    receptor = m.get(i);
                     TercerProyecto t1= new TercerProyecto();
                t1.ArchivoTipoArbolBinario(emisor, receptor, mensaje, asunto,nombreLista, fecha,"0");
                receptor="";
                }
                MensajeriaListas m1 = new MensajeriaListas();
                m1.setVisible(true);
                this.setVisible(false);
            }
            if(condicion == false)
            {
                JOptionPane.showMessageDialog(null,"Lista No existente pruebe de nuevo");
                NombreLista.setText("");
                NombreLista.setText("");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Debe de llenar todos los campos");
        }
    }//GEN-LAST:event_EnviarActionPerformed

